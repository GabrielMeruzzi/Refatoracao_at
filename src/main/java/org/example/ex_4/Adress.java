package org.example.ex_4;

public class Adress {
    private String rua;
    private String cep;

    public Adress(String rua, String cep) {
        this.rua = rua;
        this.cep = cep;
    }

    public String getRua() {
        return rua;
    }

    public String getCep() {
        return cep;
    }
}
